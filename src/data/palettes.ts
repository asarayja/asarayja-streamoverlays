/** Public palettes: core tokens authored above, the rest derived. */
export const PALETTES: Palette[] = DEFS.map((def) => ({
  ...def,
  theme: completeTheme(def.theme),
}));

export const DEFAULT_PALETTE_ID = "purple-neon";

const BY_ID = new Map(PALETTES.map((p) => [p.id, p]));

export function getPalette(id: string): Palette {
  return BY_ID.get(id) ?? PALETTES[0];
}

export function paletteTags(id: string): StyleTag[] {
  return getPalette(id).tags as StyleTag[];
}

export const CORE_PALETTES = PALETTES.filter((p) => p.collection === "core");
export const GOTHIC_PALETTES = PALETTES.filter((p) => p.collection === "gothic");
export const PRIDE_PALETTES = PALETTES.filter((p) => p.collection === "pride");
